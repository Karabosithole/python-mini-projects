"""Python dictionary is like hash tables in any other language with the time complexity of O(1).
It is an unordered collection of data values, used to store data values like a map, which, unlike 
other Data Types that hold only a single value as an element, Dictionary holds the key:value pair. 
Key-value is provided in the dictionary to make it more optimized. 

Indexing of Python Dictionary is done with the help of keys. These are of any hashable type 
i.e. an object whose can never change like strings, numbers, tuples, etc. We can create a dictionary 
by using curly braces ({}) or dictionary comprehension."""